import csv


def csvline_to_line(csvline, account_config):
    csvline = csvline.splitlines()
    csv.register_dialect(
        'test_dialect',
        account_config.get_csv_dialect()
    )
    c = csv.reader(csvline, 'test_dialect')
    return next(c)
